export const getDistrictFromNet = (net) => {
  if (!net) return '';
  const n = String(net);
  if (n === '11') return '中西區';
  if (n === '12') return '灣仔區';
  if (['14','16'].includes(n)) return '東區';
  if (n === '18') return '南區';
  if (['31','32'].includes(n)) return '油尖旺區';
  if (['34','35','41'].includes(n)) return '九龍城區';
  if (n === '40') return '深水埗區';
  if (['43','45'].includes(n)) return '黃大仙區';
  if (['46','48'].includes(n)) return '觀塘區';
  if (n === '62') return '荃灣區';
  if (['64','65','66'].includes(n)) return '葵青區';
  if (['70','71'].includes(n)) return '屯門區';
  if (['72','73','74'].includes(n)) return '元朗區';
  if (['80','81','83'].includes(n)) return '北區';
  if (n === '84') return '大埔區';
  if (['88','89','91'].includes(n)) return '沙田區';
  if (n === '95') return '西貢區';
  if (['97','98'].includes(n)) return '離島區';
  if (n === '99') return '直資私立(不限地區)';
  return '';
};

